package com.yusys.bione.frame.authconfig.web.vo;

public class BioneAuthVO {

	private String authTypeNo;
	private String defName;
	private String defNo;
	private String beanName;
	public String getAuthTypeNo() {
		return authTypeNo;
	}
	public void setAuthTypeNo(String authTypeNo) {
		this.authTypeNo = authTypeNo;
	}
	public String getDefName() {
		return defName;
	}
	public void setDefName(String defName) {
		this.defName = defName;
	}
	public String getDefNo() {
		return defNo;
	}
	public void setDefNo(String defNo) {
		this.defNo = defNo;
	}
	public String getBeanName() {
		return beanName;
	}
	public void setBeanName(String beanName) {
		this.beanName = beanName;
	}
	
	
}
