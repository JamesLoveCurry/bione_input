package com.yusys.biapp.input.index.web.vo;

public class DimFilterVO {

	
	private String dimNo;
	private String dimNm;
	private String idxNo;
	private String filterMode;
	private String filterText;
	private String filterVal;
	public String getDimNo() {
		return dimNo;
	}
	public void setDimNo(String dimNo) {
		this.dimNo = dimNo;
	}
	public String getFilterMode() {
		return filterMode;
	}
	public void setFilterMode(String filterMode) {
		this.filterMode = filterMode;
	}
	public String getFilterVal() {
		return filterVal;
	}
	public void setFilterVal(String filterVal) {
		this.filterVal = filterVal;
	}
	public String getFilterText() {
		return filterText;
	}
	public void setFilterText(String filterText) {
		this.filterText = filterText;
	}
	public String getDimNm() {
		return dimNm;
	}
	public void setDimNm(String dimNm) {
		this.dimNm = dimNm;
	}
	public String getIdxNo() {
		return idxNo;
	}
	public void setIdxNo(String idxNo) {
		this.idxNo = idxNo;
	}
	
	
	
}
